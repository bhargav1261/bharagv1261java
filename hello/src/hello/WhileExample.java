package hello;

public class WhileExample {

	public static void main(String[] args) {
		/*
		 * int i=1;
		 * 
		 * while(i<=10) { System.out.println(i); i++;
		 * 
		 * }
		 */

		int number=4;
		
		int i=1;
		while(i<=10)
		{
			System.out.println(number+"*"+i+"="+number*i);
			
			i++;
			
		}

	}

}
