package hello;

public class ForLoopExample {

	public static void main(String[] args) {
		
		/*
		 * for(int i=1;i<=100;i++) 
		 * { 
		 * 
		 * System.out.println(i); 
		 * }
		 */
		int number=25;
		for (int i=1;i<=10;i++)
		{
			System.out.println(number+"*"+i+"="+number*i);
		}
	}

}
